package com.letstalk.javaclass;

public class ChatMessage {

    private String txt;
    private String sender;

    public ChatMessage() {
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public String getSender() {
        return sender;
    }

    public void setSender(String sender) {
        this.sender = sender;
    }

}
